from aiogram import Router, F
from aiogram.types import Message, CallbackQuery
from aiogram.utils.keyboard import InlineKeyboardBuilder

router = Router()

EFFECTIVE_DATE = "11.03.2026"
SUPPORT_USERNAME = "@jeremyClar"
SUPPORT_EMAIL = "youfreeth@gmail.com"
SITE_URL = "freeth.ru"
SERVICE_NAME = "Freeth"


def documents_keyboard():
    builder = InlineKeyboardBuilder()
    builder.button(text="Пользовательское соглашение", callback_data="doc_user_agreement")
    builder.button(text="Политика возвратов", callback_data="doc_refund_policy")
    builder.button(text="Политика конфиденциальности", callback_data="doc_privacy_policy")
    builder.adjust(1)
    return builder.as_markup()


@router.message(F.text == "Документы")
async def documents_menu(message: Message):
    await message.answer(
        "Документы сервиса:\n\n"
        "Здесь доступны:\n"
        "• пользовательское соглашение\n"
        "• политика возвратов\n"
        "• политика конфиденциальности\n\n"
        f"Дата вступления в силу: {EFFECTIVE_DATE}",
        reply_markup=documents_keyboard(),
    )


