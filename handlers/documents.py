from aiogram import Router, F
from aiogram.types import Message, CallbackQuery
from aiogram.utils.keyboard import InlineKeyboardBuilder

router = Router()


def documents_keyboard():
    builder = InlineKeyboardBuilder()
    builder.button(text="Пользовательское соглашение", callback_data="doc_user_agreement")
    builder.button(text="Политика возвратов", callback_data="doc_refund_policy")
    builder.button(text="Политика конфиденциальности", callback_data="doc_privacy_policy")
    builder.adjust(1)
    return builder.as_markup()


@router.message(F.text == "Документы")
async def documents_menu(message: Message):
    await message.answer(
        "Документы сервиса:\n\n"
        "Здесь доступны:\n"
        "• пользовательское соглашение\n"
        "• политика возвратов\n"
        "• политика конфиденциальности",
        reply_markup=documents_keyboard(),
    )


@router.callback_query(F.data == "doc_user_agreement")
async def doc_user_agreement(callback: CallbackQuery):
    await callback.message.answer(
        "Пользовательское соглашение\n\n"
        "1. Сервис предоставляет пользователю цифровую услугу защищенного сетевого подключения "
        "на условиях выбранного тарифа.\n"
        "2. Доступ предоставляется на срок, указанный в тарифе: 1 месяц, 3 месяца, 12 месяцев "
        "или пробный период 7 дней.\n"
        "3. Услуга предназначена для личного использования, защиты сетевого трафика и безопасной "
        "работы в публичных или недоверенных сетях.\n"
        "4. Пользователь самостоятельно несет ответственность за соблюдение законодательства страны "
        "своего пребывания и правил использования сети интернет.\n"
        "5. Передача доступа третьим лицам, перепродажа или массовое использование сервиса без "
        "согласования с владельцем запрещены.\n"
        "6. Доступ может быть ограничен или прекращен при нарушении условий использования, "
        "попытках злоупотребления сервисом или действиях, создающих угрозу стабильности работы сервиса.\n"
        "7. Поддержка по вопросам подключения и работы сервиса предоставляется через контакт поддержки, "
        "указанный в боте.\n"
        "8. Оплата услуги означает согласие пользователя с настоящими условиями."
    )
    await callback.answer()


@router.callback_query(F.data == "doc_refund_policy")
async def doc_refund_policy(callback: CallbackQuery):
    await callback.message.answer(
        "Политика возвратов\n\n"
        "1. Возврат возможен, если оплаченный доступ не был предоставлен пользователю "
        "по технической причине на стороне сервиса.\n"
        "2. Если доступ был успешно выдан и услуга начала использоваться, возврат рассматривается индивидуально.\n"
        "3. Для запроса возврата пользователь должен обратиться в поддержку, указать свой Telegram ID, "
        "дату оплаты и кратко описать проблему.\n"
        "4. Заявки на возврат рассматриваются в разумный срок после обращения пользователя.\n"
        "5. При подтверждении технической ошибки со стороны сервиса возврат осуществляется способом, "
        "допустимым правилами платежного провайдера."
    )
    await callback.answer()


@router.callback_query(F.data == "doc_privacy_policy")
async def doc_privacy_policy(callback: CallbackQuery):
    await callback.message.answer(
        "Политика конфиденциальности\n\n"
        "1. Для работы сервиса могут обрабатываться следующие данные: Telegram ID, имя профиля Telegram, "
        "сведения о выбранном тарифе, статус подписки и технические данные, необходимые для предоставления доступа.\n"
        "2. Данные используются исключительно для выдачи доступа, сопровождения подписки, обработки оплаты "
        "и поддержки пользователей.\n"
        "3. Данные не передаются третьим лицам, кроме случаев, когда это необходимо для обработки платежа "
        "или требуется по закону.\n"
        "4. Пользователь может обратиться в поддержку по вопросам, связанным с обработкой его данных.\n"
        "5. Использование бота означает согласие пользователя с настоящей политикой."
    )
    await callback.answer()